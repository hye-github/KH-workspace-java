import java.lang.*; // 생략 가능
public class Exam_01 // 파일명과 똑같이
{
 public static void main(String[] ar) // 초기 실행위치
 {
	 System.out.println(); // sysout > ctrl + space
	 
	 System.out.println("Hello world");
	 System.out.println("나는 누구이다.");
	 System.out.println(10);
	 System.out.println('A');
	 System.out.println('가');
	 System.out.println('ㄱ');
	 System.out.println(3.14);
	 
	 System.out.println("150" + 100);
	 System.out.println(150 + 100);
	 System.out.println('A' + 10);
	 System.out.println('가' + 10);
	 System.out.println("A" + 10);
	 
	 // 자바에서 문자열을 표현할 때는 " " 를 사용한다.
	 // 자바에서 문자를 표현할 때는 ' ' 를 사용한다.
	 // 자바에서 숫자를 표현할 때는 Quote 를 사용하지 않는다.
	 
 }
}
